           config.getInitParameter(
                Constants.SERVLET_INIT_PARAM_ACCESS_DELETE_JOB);
      if ((resourceDNDeleteJob == null) || (resourceDNDeleteJob.length() == 0))
      {
        resourceDNDeleteJob =
             configProperties.getProperty(
                  Constants.SERVLET_INIT_PARAM_ACCESS_DELETE_JOB);
      }


